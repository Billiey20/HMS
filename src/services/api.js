// ═══════════════════════════════════════════════════════════════════
// LAB ORDERS
// ═══════════════════════════════════════════════════════════════════
export const labService = {
  /** All lab orders for today */
  async list({ status = null } = {}) {
    let q = supabase
      .from('lab_orders')
      .select(`
        *,
        patients(patient_no, first_name, last_name, age, gender),
        lab_order_items(*)
      `)
      .order('ordered_at', { ascending: false });

    if (status) q = q.eq('status', status);
    return throwIf(await q);
  },

  async create(orderPayload, tests = []) {
    const order = throwIf(
      await supabase.from('lab_orders').insert([orderPayload]).select().single()
    );
    if (tests.length) {
      throwIf(
        await supabase.from('lab_order_items').insert(
          tests.map(t => ({ lab_order_id: order.id, test_name: t, status: 'pending' }))
        )
      );
    }
    return order;
  },

  async updateStatus(id, status) {
    return throwIf(
      await supabase.from('lab_orders').update({ status }).eq('id', id)
    );
  },

  /** Save results for specific test items */
  async saveResults(orderId, items) {
    // items: [{ id, result }]
    const updates = items.map(i =>
      supabase.from('lab_order_items').update({ result: i.result, status: 'completed', result_at: new Date().toISOString() }).eq('id', i.id)
    );
    await Promise.all(updates);
    // Mark order as completed
    await supabase.from('lab_orders').update({ status: 'completed' }).eq('id', orderId);
  },

  async pendingCount() {
    const { count } = await supabase
      .from('lab_orders')
      .select('*', { count: 'exact', head: true })
      .eq('status', 'pending');
    return count || 0;
  },
};

