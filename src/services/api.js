// WARDS / BEDS / ADMISSIONS
// ═══════════════════════════════════════════════════════════════════
export const ipdService = {
  async getBedOccupancy() {
    return throwIf(await supabase.from('v_bed_occupancy').select('*'));
  },

  async listBeds(wardId = null) {
    let q = supabase
      .from('beds')
      .select('*, wards(name, ward_type), admissions(*, patients(first_name, last_name, patient_no, age, gender))')
      .eq('admissions.status', 'active');
    if (wardId) q = q.eq('ward_id', wardId);
    return throwIf(await q);
  },

  async listAdmissions() {
    return throwIf(
      await supabase.from('v_current_inpatients').select('*')
    );
  },

  async admit(payload) {
    // Update bed status
    if (payload.bed_id) {
      await supabase.from('beds').update({ status: 'occupied' }).eq('id', payload.bed_id);
    }
    return throwIf(
      await supabase.from('admissions').insert([payload]).select().single()
    );
  },

  async discharge(admissionId, payload) {
    const { data: adm } = await supabase.from('admissions').select('bed_id').eq('id', admissionId).single();
    if (adm?.bed_id) {
      await supabase.from('beds').update({ status: 'available' }).eq('id', adm.bed_id);
    }
    return throwIf(
      await supabase.from('admissions')
        .update({ status: 'discharged', discharged_at: new Date().toISOString(), ...payload })
        .eq('id', admissionId)
    );
  },

  async addNote(payload) {
    return throwIf(
      await supabase.from('clinical_notes').insert([payload]).select().single()
    );
  },

  async addVitals(payload) {
    return throwIf(
      await supabase.from('vitals_records').insert([payload]).select().single()
    );
  },

  async getVitals(patientId, admissionId) {
    return throwIf(
      await supabase
        .from('vitals_records')
        .select('*')
        .eq('patient_id', patientId)
        .eq('admission_id', admissionId)
        .order('recorded_at', { ascending: false })
        .limit(20)
    );
  },

  async activeCount() {
    const { count } = await supabase
      .from('admissions')
      .select('*', { count: 'exact', head: true })
      .eq('status', 'active');
    return count || 0;
  },
};

