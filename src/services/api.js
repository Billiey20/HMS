import { supabase } from '../lib/supabase';

// ── helpers ──────────────────────────────────────────────────────────
const throwIf = ({ data, error }) => {
  if (error) throw error;
  return data;
};

// ═══════════════════════════════════════════════════════════════════
// PATIENTS
// ═══════════════════════════════════════════════════════════════════
export const patientService = {
  /** Fetch all patients ordered by most recent */
  async list({ search = '' } = {}) {
    let q = supabase
      .from('patients')
      .select('*')
      .order('created_at', { ascending: false });

    if (search) {
      q = q.or(
        `first_name.ilike.%${search}%,last_name.ilike.%${search}%,patient_no.ilike.%${search}%,phone.ilike.%${search}%`
      );
    }
    return throwIf(await q);
  },

  async get(id) {
    return throwIf(await supabase.from('patients').select('*').eq('id', id).single());
  },

  async create(payload) {
    // patient_no autogenerated by DB trigger
    return throwIf(
      await supabase.from('patients').insert([payload]).select().single()
    );
  },

  async update(id, payload) {
    return throwIf(
      await supabase.from('patients').update(payload).eq('id', id).select().single()
    );
  },

  async count() {
    const { count } = await supabase.from('patients').select('*', { count: 'exact', head: true });
    return count || 0;
  },
};

